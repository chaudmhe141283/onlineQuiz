package model;

/**
 *
 * @author Admin
 */
public class User {
    private String user;
    private String pass;
    private int type;
    private String email;

    public User(String user, String pass, int type, String email) {
        this.user = user;
        this.pass = pass;
        this.type = type;
        this.email = email;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "User{" + "user=" + user + ", pass=" + pass + ", type=" + type + ", email=" + email + '}';
    }
    
    
}
